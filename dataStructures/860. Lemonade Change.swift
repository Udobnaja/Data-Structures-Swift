import Foundation

func lemonadeChange(_ bills: [Int]) -> Bool {
    var five = 0
    var ten = 0
    
    for bill in bills {
        switch bill {
        case 5:
            five += 1
        case 10:
            if five == 0 {
                return false
            }
            five -= 1
            ten += 1
        case 20:
            if five == 0 {
                return false
            }
            
            if ten == 0 {
                if five >= 3 {
                    five -= 3
                } else {
                    return false
                }
            } else {
                five -= 1
                ten -= 1
            }
            
        default:
            break
        }
    }
    
    return true
}

